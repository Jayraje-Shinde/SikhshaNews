// ── Roles ─────────────────────────────────────────────────
export type Role = 'SUPER_ADMIN' | 'ADMIN' | 'EDITOR';

// ── User / Auth ───────────────────────────────────────────
export interface User {
  id: string;
  email: string;
  role: Role;
  institutionName?: string;
  institutionType?: 'UNIVERSITY' | 'RESEARCH_CENTER' | 'OTHER';
}

// ── Posts ─────────────────────────────────────────────────
export type PostType = 'ARTICLE' | 'RESEARCH_PAPER' | 'IPR_DOCUMENT';
export type PostStatus = 'DRAFT' | 'PUBLISHED' | 'ARCHIVED';
export type AiStatus = 'PENDING' | 'PROCESSING' | 'COMPLETED' | 'FAILED';

export interface Post {
  id: string;
  title: string;
  slug: string;
  excerpt?: string;
  content: string;
  summary?: string;            // AI-generated by Claude Haiku 4.5
  imageUrl?: string;           // Cloudinary URL — cover image
  documentUrl?: string;        // PDF URL for research papers / IPR docs
  postType: PostType;
  status: PostStatus;
  aiProcessingStatus: AiStatus;
  views: number;
  isPinned: boolean;
  categories: Category[];
  tags: Tag[];
  createdAt: string;
  updatedAt: string;
  publishedAt?: string;
}

// ── Categories & Tags ─────────────────────────────────────
export interface Category {
  id: string;
  name: string;
}

export interface Tag {
  id: string;
  name: string;
}

// ── Comments ──────────────────────────────────────────────
export interface Comment {
  id: string;
  postId: string;
  parentId?: string;
  guestName: string;
  content: string;
  isApproved: boolean;
  isFlagged: boolean;
  createdAt: string;
}

// ── API Response wrappers ─────────────────────────────────
export interface PaginatedResponse<T> {
  data: T[];
  pagination: {
    total: number;
    page: number;
    limit: number;
    totalPages: number;
  };
}

// ── Nav ───────────────────────────────────────────────────
export interface NavItem {
  label: string;
  path: string;
  icon?: React.ReactNode;
  children?: NavItem[];
}
